"""! @file generator.py"""

import abc
from pathlib import Path

from SIG.topic_generator.topic import Topic

########################################################################################################################
# ABSTRACT GENERATOR CLASS
########################################################################################################################
class Generator(abc.ABC):

    ####################################################################################################################
    # PUBLIC
    ####################################################################################################################

    ##==================================================================================================================
    #
    def __init__(self, f: Path, p: Topic):
        self.generate(f,p)
        return

    ##==================================================================================================================
    #
    @abc.abstractmethod
    def generate(self):
        pass

    ####################################################################################################################
    # PRIVATE
    ####################################################################################################################
